def print_b(b):
  print('\n'.join(map(lambda e: ' '.join(e), b)))

def put_piece(x, y, p, b):
  if y+len(p) >= len(b) or x+len(p[0]) > len(b[0]):
    return
  for e in p:
    for i, v in enumerate(e):
      b[y][x+i] = v
  return True

